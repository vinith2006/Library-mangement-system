public class Book {
    public int id;
    public String title;
    public String author;
    public int year;
    public boolean available;

    public Book(int id, String title, String author, int year, boolean available) {
        this.id = id;
        this.title = title;
        this.author = author;
        this.year = year;
        this.available = available;
    }
}
